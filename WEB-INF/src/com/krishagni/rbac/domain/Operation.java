package com.krishagni.rbac.domain;

import org.hibernate.envers.Audited;

@Audited
public class Operation {
	private Long id;
	
	private String name;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
