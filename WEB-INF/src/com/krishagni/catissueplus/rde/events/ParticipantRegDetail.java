package com.krishagni.catissueplus.rde.events;

import java.util.Date;

public class ParticipantRegDetail {
	private String cpShortTitle;

	private Long cpId;

	private Long cprId;

	private String empi;

	private String ppid;

	private Date regDate;

	private String nextEventLabel;

	private String errorMessage;

	public String getCpShortTitle() {
		return cpShortTitle;
	}

	public void setCpShortTitle(String cpShortTitle) {
		this.cpShortTitle = cpShortTitle;
	}

	public Long getCpId() {
		return cpId;
	}

	public void setCpId(Long cpId) {
		this.cpId = cpId;
	}

	public Long getCprId() {
		return cprId;
	}

	public void setCprId(Long cprId) {
		this.cprId = cprId;
	}

	public String getEmpi() {
		return empi;
	}

	public void setEmpi(String empi) {
		this.empi = empi;
	}

	public String getPpid() {
		return ppid;
	}

	public void setPpid(String ppid) {
		this.ppid = ppid;
	}

	public Date getRegDate() {
		return regDate;
	}

	public void setRegDate(Date regDate) {
		this.regDate = regDate;
	}

	public String getNextEventLabel() {
		return nextEventLabel;
	}

	public void setNextEventLabel(String nextEventLabel) {
		this.nextEventLabel = nextEventLabel;
	}

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
}
